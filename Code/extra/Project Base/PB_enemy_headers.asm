; ::: Enemy Headers :::
;Enemy headers are in the format:
;org $A0xxxx							;location of enemy header, all of which are stored in A0
;dw $dddd								;amount of tile data to transfer to VRAM (1 tile = $20) (>6000 = using misc VRAM)
;dw $pppp								;palette pointer
;dw $hhhh,$dddd							;enemy health, damage to samus on contact
;dw $xxxx,$yyyy							;hitbox pixel width / 2, hitbox pixel height / 2
;db $bb									;bank byte
;db $hh									;number of hurt ai frames when hit (+8 = number of frames for hurt flash)
;dw $ssss, $bbbb						;sound played when enemy is hit, something for boss fights
;dw $Init								;initialization ai pointer
;dw $pppp, $0000						;number of parts to the enemy (rarely used, otherwise 0001), unknown
;dw $Running							;running ai pointer
;dw $gggg, $hhhh, $ffff, $pppp			;reaction pointers: grapple, hit, frozen ai, pause ai
;dw $aaaa, $0000						;enemy death animation, unknown
;dw $pppp, $0000, $tttt, $pppp, $0000	;reaction pointers: power bomb, unknown, touch, projectile, unknown
;dw $0000, $0000, $0000, $0000			;unknown
;dl $pppppp								;enemy gfx pointer (000000 = using misc VRAM)
;db $ll									;layer priority control ($02 = in front of samus, $05 = between samus and background, $0B = behind background)
;dw $iiii, $rrrr						;item drop chances (found in bank $B4), projectile resistance pointer
;dw $nnnn								;enemy name

;where the grapple ai reactions are:
;$00xx				Crash
;$8000				Ignores enemy, enemy pauses one frame. Unfreezes frozen enemies
;$8001-04, 06, 0E	Ignores enemy, enemy movement stops
;$8005				latches on to enemy, enemy movement stops until released
;$8007-08, 0B-0D	Curves, enemy unaffected
;$8009				Enemy stops
;$800A				Latches on to and kills enemy
;$800F				Stopped by enemy, enemy not affected
;$8014				Latches on to enemy with no effect on enemy
;$801E				Latches on to enemy for a moment, hurts and knocks samus off
;$8023				Latches on to enemy for a moment, samus is hurt once a frame for 1 damage until killed
;$8028				Latches on to enemy for a moment, samus is hurt once a frame for 1 damage until killed and enemy movement is stopped
;$8037				Ignores and kills enemy
;$803C				Ignores enemy, stops enemy movement, and enemy becomes untouchable
;$804B				Crash

